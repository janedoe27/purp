    {
        $input = $request->all();

        $session = new Session([
            'question_id' => $input['question'],
            'answer_id' => $input['answer'],
            'interviewee_id' => Auth::user()->id
        ]);

        $session->save();
        
        return redirect()->action('TestController@new')->with("status", "Your Answer was submitted successfully."); 
